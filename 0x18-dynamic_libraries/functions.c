#include "functions.h"
/**
 * add - adds two integers and returns the result
 * @i1: first input integer
 * @i2: second input integer
 *
 * Return: result
*/
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - substract two integers and returns the result
 * @i1: first input integer
 * @i2: second input integer
 *
 * Return: result
*/
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - substract two integers and returns the result
 * @i1: first input integer
 * @i2: second input integer
 *
 * Return: result
*/
int mul(int a, int b)
{
        return (a * b);
}
/**
 * div - substract two integers and returns the result
 * @i1: first input integer
 * @i2: second input integer
 *
 * Return: result
*/
int div(int a, int b)
{
        return (a / b);
}
/**
 * mod - substract two integers and returns the result
 * @i1: first input integer
 * @i2: second input integer
 *
 * Return: result
*/
int mod(int a, int b)
{
        return (a % b);
}
